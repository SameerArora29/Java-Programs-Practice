public class Pair {
	
	int first;
	int second;
	
	public Pair(int first,int second) {
		this.first=first;
		this.second=second;
	}
	
	public void setfirst(int first) {
		this.first = first;
	}
	public int getfirst() {
		return first;
	}
	
	public int getsecond() {
		return second;
	}
	
	public void setsecond(int second) {
		this.second=second;
	}
}